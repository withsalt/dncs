        public static bool Initialize(CustumDbContext context)
        {
            // 检测数据库中是否存在该库及表、若用现有的库则不会创建表、导致后面save时报错
            if (!context.Database.EnsureCreated())
            {
                context.SystemLog.Add(new SystemLog()
                {
                    LogType = "system",
                    Describe = "AS.Service start.",
                    Stack = "",
                    Localtion = "ELDbDataInitializer",
                    CreateTime = TimeUtil.Timestamp()
                });
                context.SaveChanges();
                return true;
            }

            using (var trans = context.Database.BeginTransaction())
            {
                try
                {
                    List<Role> roles = CreateDefaultRoles();
                    context.Role.AddRange(roles);
                    context.UserInfo.AddRange(CreateDefaultUserInfo(roles));
                    context.ContentType.AddRange(CreateDefaultContentType());
                    if (ConfigManager.Now.AppSettings.IsDebug)
                    {
                        context.ContentInfo.AddRange(CreateDefaultContentInfo());
                    }
                    context.SystemLog.Add(new SystemLog()
                    {
                        LogType = "system",
                        Describe = "firest inti db.",
                        Stack = "",
                        Localtion = "CustumDbDataInitializer",
                        CreateTime = TimeUtil.Timestamp()
                    });
                    //添加uuid-ossp扩展
                    context.Database.ExecuteSqlRaw("CREATE EXTENSION IF NOT EXISTS \"uuid-ossp\"");
                    if (context.SaveChanges() <= 0)
                    {
                        trans.Rollback();
                        return false;
                    }
                    else
                    {
                        trans.Commit();
                        return true;
                    }
                }
                catch (Exception ex)
                {
                    trans.Rollback();
                    throw new Exception($"Init db data failed, error is {ex.Message}");
                }
            }
        }

